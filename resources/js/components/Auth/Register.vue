<template>
    <div class="flex items-center justify-center h-screen">
        <div class="w-full max-w-md">
            <form class="bg-white shadow-md rounded px-8 pt-6 pb-8 mb-4 shadow-custom">
                <div class="text-center mb-10">
                    <div class="text-gray-900 text-3xl font-medium">
                        <img src="../../../../public/image/0a29b111-f86f-4c98-a56e-1c0c6cc2881f.png" class="mx-auto">
                    </div>
                </div>

                <div class="mb-4">
                    <label class="block text-gray-700 text-sm font-bold mb-2" for="name">
                        İsim
                    </label>
                    <input
                        class="shadow appearance-none border rounded w-full py-2 px-3 text-gray-700 leading-tight focus:outline-none focus:shadow-outline"
                        type="text"
                        placeholder="İsim"
                        v-model="user.name"
                    />
                </div>
                <div class="mb-4">
                    <label class="block text-gray-700 text-sm font-bold mb-2" for="email">
                        Email
                    </label>
                    <input
                        class="shadow appearance-none border rounded w-full py-2 px-3 text-gray-700 leading-tight focus:outline-none focus:shadow-outline"
                        type="email"
                        placeholder="Email"
                        v-model="user.email"
                    />
                </div>
                <div class="mb-6">
                    <label class="block text-gray-700 text-sm font-bold mb-2" for="password">
                        Şifre
                    </label>
                    <input
                        class="shadow appearance-none border rounded w-full py-2 px-3 text-gray-700 mb-3 leading-tight focus:outline-none focus:shadow-outline"
                        type="password"
                        placeholder="******************"
                        v-model="user.password"
                    />
                </div>

                <div class="flex items-center justify-between">
                    <p class="text-gray-500 mr-3">Zaten Hesabım Var, <a @click="goToLogin" class="text-[#00B3D7]  hover:text-[#0095B0]  cursor-pointer">Giriş Yap</a></p>
                    <button
                        class="bg-green-500 hover:bg-green-700 text-white font-bold py-2 px-4 rounded focus:outline-none focus:shadow-outline"
                        type="button"
                        @click="register"
                        v-tooltip="'Kayıt Ol'"
                    >
                        Kayıt Ol
                    </button>
                </div>
            </form>
        </div>
    </div>
</template>

<script setup>
import { ref } from 'vue';
import axios from 'axios';
import { useRouter } from 'vue-router';
import { useToast } from 'vue-toastification';
import { useReCaptcha } from 'vue-recaptcha-v3';

const toast = useToast();
const router = useRouter();
const user = ref({
    name: null,
    email: null,
    password: null,
});

const { executeRecaptcha } = useReCaptcha();

const register = async () => {
    try {
        const token = await executeRecaptcha('register');
        const response = await axios.post('/api/register', {
            ...user.value,
            'g-recaptcha-response': token,
        });

        toast.success(response.data.message);
        await router.push({ name: 'login' });
    } catch (error) {
        toast.error('Kayıt başarısız. Lütfen tekrar deneyin.');
    }
};

const goToLogin = () => {
    router.push({ name: 'login' });
};
</script>

